/*
    OPERADORES DE COMPARACIÓN
*/

//IGUAL A: Compara si dos valores son iguales
let igualA = 5 == "5";
console.log(igualA);

//IGUAL A ESTRICTO: Compara si dos valores son iguales en VALOR Y TIPO
let igualEstricto = 5 === "5";
console.log(igualEstricto);

//NO IGUAL A: Compara si dos valores NO son iguales sin tener en cuenta el tipo
let noIgual = 5 != "5";
console.log(noIgual);

//NO IGUAL A ESTRICTO: Compara si dos valores NO son iguales en valor y tipo
let noEstricto = 5 !== "5";
console.log(noEstricto);

//MAYOR QUE
let mayor = 10 > 5;
console.log(mayor);

//MENOR QUE
let menor = 4 < 10;
console.log(menor);

//MAYOR O IGUAL QUE
let mayorIgual = 10 >= 10;
console.log(mayorIgual);

//MENOR O IGUAL QUE
let menorIgual = 5 <= 2;
console.log(menorIgual);

